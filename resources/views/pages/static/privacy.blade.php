<h4><a name="cookies"></a>2. Informativa sull’utilizzo dei cookies</h4>
<p><em>Informativa Cookie</em>, ai sensi dell’ art. 13 del Codice Privacy e dell’art. 13 del GDPR e Provvedimento
    generale del Garante privacy dell’8 maggio 2014.</p>
<p><strong>Cosa sono i cookie</strong></p>
<p>I cookie sono piccole porzioni di dati che il server web fornisce al dispositivo di navigazione dell’utente
    (<em>computer, tablet, smartphone</em>, ecc.) per consentire l’utilizzo dei nostri servizi. Inoltre le informazioni
    generate dall’uso dei cookie ci permettono di ottimizzare le pagine e le nostre infrastrutture tecniche, migliorando
    così la navigazione del sito.</p>
<p><strong>Catalogazione dei cookies</strong></p>
<ul>
    <li><em>cookies tecnici</em>, strettamente necessari per far funzionare il sito web e per fornire il servizio
        offerto e richiesto dall’utente;</li>
    <li><em>cookies di profilazione,</em>&nbsp;utilizzati per creare profili relativi all’utente in base alle sue scelte
        e preferenze, al fine di inviare messaggi pubblicitari in linea con il profilo creato.</li>
</ul>
<p><strong>Cookie utilizzati da questo sito e loro finalità</strong></p>
<ul>
    <li><em>cookies di analisi anonimi;</em></li>
    <li><em>cookies di terze parti;</em></li>
</ul>
<p>Utilizza i dati raccolti con i cookies al fine di offrire all’utente la miglior esperienza possibile sul proprio sito.
    I cookies generati dalla navigazione sono utilizzati esclusivamente per il funzionamento stesso dell'applicazione.</p>
<p><strong>Cookies di profilazione o pubblicità<br /> </strong></p>
<p>Il nostro sito non utilizza cookies di profilazione o pubblicità. I cookies di profilazione sono quei cookies
    necessari a creare profili utente al fine di inviare messaggi pubblicitari in linea con le preferenze manifestate
    dall’utente all’interno delle pagine del sito. Secondo la normativa vigente (Direttiva 2009/136 che ha modificato la
    direttiva e-Privacy 2002/58/CE e provvedimento per l’individuazione delle modalità semplificate per l’informativa e
    l’acquisizione del consenso per l’uso dei cookie – 8 maggio 2014). Il nostro sito non è tenuto a chiedere consenso
    per i cookie tecnici e di analytics, in quanto necessari a fornire i servizi richiesti.</p>
<p>Individuazione delle modalità semplificate per l’informativa e l’acquisizione del consenso per l’uso dei cookie – 8
    maggio 2014 [3118884]</p>
