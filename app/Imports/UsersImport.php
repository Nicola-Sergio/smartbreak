<?php

namespace App\Imports;

use App\Models\User;
use Maatwebsite\Excel\Concerns\ToModel;
use Illuminate\Support\Facades\Hash;
use Maatwebsite\Excel\Concerns\WithHeadingRow;
use Maatwebsite\Excel\Concerns\WithChunkReading;
use Maatwebsite\Excel\Concerns\WithBatchInserts;
use Maatwebsite\Excel\Concerns\WithUpserts;
use Illuminate\Contracts\Queue\ShouldQueue;


class UsersImport implements ToModel, WithHeadingRow, WithBatchInserts, WithChunkReading, WithUpserts, ShouldQueue
{
    /**
    * @param array $row
    *
    * @return \Illuminate\Database\Eloquent\Model|null
    */
    public function model(array $row)
    {
        return new User([
            'first_name'    => $row['first_name'],
            'last_name'     => $row['last_name'],
            'email'         => $row['email'], 
            'password'      => Hash::make($this->rand_password()),
            'role'          => 'STUDENT',
            'class_id'      => $row['class_id'],
            'site_id'       => $row['site_id'],
        ]);
    }

    public function batchSize(): int
    {
        return 500;
    }
    
    public function chunkSize(): int
    {
        return 500;
    }

    public function uniqueBy()
    {
        return 'email';
    }

    /**
     * rand_password.
     * Genera una password casuale di 12 caratteri
     *
     * @access	private
     * @return	string
     */
    private function rand_password() {
        $chars = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!Â£$%&/()=?@#*+-";
        return substr(str_shuffle($chars),0,12);
    }
}
